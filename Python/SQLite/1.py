# What is a Database?

# A database is an organized collection of data that can be easily

# stored, managed, and retrieved using software called a Database Management System (DBMS).
 
# SQLite Database
 
# SQLite is a lightweight, serverless, and self-contained database 

# engine that stores the entire database in a single file on your device.

# File-based: the entire database (tables, indexes, data) is stored in a single file (e.g. robot.db).

# Serverless: there is no separate database server process to install or run.

# Zero-configuration: you can start using it immediately; Python includes support via the sqlite3 module.
 
# Why SQLite suits robotics projects

# No internet or server needed — robots in the field can store logs locally.

# Low footprint — small memory and disk use, ideal for embedded hardware.

# Simplicity — easy to integrate into Python scripts used for robot control and logging.

# Single file — easy to move, copy, or back up logs from robot to a computer.
 
# DB Browser for SQLite is a free, open-source software that 

# lets you view, create, edit, and manage SQLite databases with a 

# graphical interface (GUI) — no need to write SQL commands every time.