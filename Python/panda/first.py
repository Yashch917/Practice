# Definition:
# Pandas is a Python library used for data analysis and data handling.
# It helps to store, clean, and analyze data easily — 
# just like working with Excel sheets, but using Python.

# Why Pandas?
# Without Pandas, we must handle data using lists,
# dictionaries, or NumPy arrays — which is difficult for large data.

# Pandas makes it:
# Easier to store tabular data (rows & columns)
# Easier to filter, clean, and sort data
# Perfect for CSV, Excel, and database files

# Installation:
# If Pandas is not installed, use this command:
# pip install pandas

# Then import it in Python:
# import pandas as pd
# pd is just a short name (alias) used by everyone for convenience.

# | Data Structure |        Description                                           |     Example                 |
# | -------------- | ------------------------------------------------------------ | --------------------------- |
# | **Series**     | A 1D labeled array (like a single column in Excel)           | Roll Numbers, Names, Prices |
# | **DataFrame**  | A 2D labeled table (like an Excel sheet with rows & columns) | Student table, Sales data   |